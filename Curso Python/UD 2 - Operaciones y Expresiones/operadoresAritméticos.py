# Ejemplo de operadores aritméticos en Python

a = 10
b = 3

# Suma
print("Suma:", a + b)

# Resta
print("Resta:", a - b)

# Multiplicación
print("Multiplicación:", a * b)

# División
print("División:", a / b)

# División entera
print("División entera:", a // b)

# Módulo (resto)
print("Módulo:", a % b)

# Potencia
print("Potencia:", a ** b)
